'''     CLASSWORK 
write a python program to get a number and check if it is divisible by both 2 and 3 or not.

'''

#python code for classwork
n = int(input('enter a number:\t'))
if n % 2 == 0 and n%3 == 0:
    print('divisible by both 2 and 3')
else:
    print('not divisible by 2 and 3')



